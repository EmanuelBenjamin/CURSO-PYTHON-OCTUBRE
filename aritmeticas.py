# funcion sum la cual suma varios valores como unalista
respuesta = sum((5,2,7,10))
print(respuesta)

# funcion max , me indica cual es el valor maximo en este caso el valor 10
respuesta = max((5,2,7,10))
print(respuesta)

# funcion min, en este caso me indica cual es el valor minimo
respuesta = min((5,2,7,10))
print(respuesta)

#funcion sorted , la cual devuelve ordenados los valores de menor a mayo
respuesta = sorted((5,2,7,10))
print(respuesta)